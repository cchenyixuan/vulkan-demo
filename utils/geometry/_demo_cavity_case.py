"""
_demo_cavity_case.py — build a lid-driven-cavity case structure from the tool.

A DEMO that composes the generic geometry tool into a specific case with the same
4-part split as cases/lid_driven_cavity_2d: it does NOT live in the core library
(the library stays case-agnostic). Emits, into the output case dir:

  frame.obj     — bounding-box cube (the loader derives the voxel grid from it)
  domain.obj    — fluid interior  (material stick_water)
  wall.obj      — static U-shaped wall: bottom + both sides + top-side corners (stick_wall)
  wall_top.obj  — moving lid: top band over the fluid width (stick_lid)
  case.yaml     — physics / numerics / capacities, referencing the four .obj

Layout (matches cases/lid_driven_cavity_2d): fluid box [-0.5,0.5]^2 (1 m) on a grid
lattice, surrounded by a BORDER_LAYERS-thick frame; the top band over the fluid width
is the lid, the rest of the frame is wall. h/dx = 5.

    .venv/Scripts/python.exe utils/geometry/_demo_cavity_case.py            # ~6M fluid
    .venv/Scripts/python.exe utils/geometry/_demo_cavity_case.py --half 500 --out cases/..._1m
"""

from __future__ import annotations

import argparse
import math
import pathlib
import sys

import numpy as np

_REPO_ROOT = pathlib.Path(__file__).resolve().parents[2]
if str(_REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(_REPO_ROOT))

from utils.geometry import LATTICE_GRID, tile_bounding_box     # noqa: E402

FLUID_HALF_EXTENT = 0.5            # fluid box [-0.5, 0.5]^2 = 1 m cavity (as in the existing case)


def write_obj(path, points_2d):
    points_3d = np.column_stack([points_2d, np.zeros(points_2d.shape[0])])
    np.savetxt(path, points_3d, fmt="v %.6f %.6f %.6f",
               header=f"# {points_2d.shape[0]} particles", comments="")


def write_preview(path, domain, wall, lid, dx, frame_half):
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    figure, axes = plt.subplots(1, 3, figsize=(18, 6))
    sample = np.linspace(0, domain.shape[0] - 1, min(40_000, domain.shape[0])).astype(int)
    axes[0].scatter(domain[sample, 0], domain[sample, 1], s=0.4, c="tab:blue", label="domain/fluid")
    axes[0].scatter(wall[:, 0], wall[:, 1], s=0.4, c="tab:green", label="wall")
    axes[0].scatter(lid[:, 0], lid[:, 1], s=0.4, c="tab:red", label="wall_top/lid")
    axes[0].add_patch(plt.Rectangle((-frame_half, -frame_half), 2 * frame_half, 2 * frame_half,
                                    fill=False, ec="k", lw=1.0))
    axes[0].set_title(f"fluid {domain.shape[0]:,} / wall {wall.shape[0]:,} / lid {lid.shape[0]:,}")
    axes[0].set_aspect("equal"); axes[0].legend(markerscale=12, loc="center", fontsize=8)

    def zoom(axis, x_center, y_center, title):
        window = 18 * dx
        for cloud, color in ((domain, "tab:blue"), (wall, "tab:green"), (lid, "tab:red")):
            mask = (np.abs(cloud[:, 0] - x_center) < window) & (np.abs(cloud[:, 1] - y_center) < window)
            axis.scatter(cloud[mask, 0], cloud[mask, 1], s=12, c=color)
        axis.set_title(title); axis.set_aspect("equal")
    zoom(axes[1], -0.5, 0.5, "top-LEFT corner (wall meets lid)")
    zoom(axes[2], 0.0, 0.5, "top-CENTER (lid over fluid)")
    figure.tight_layout(); figure.savefig(path, dpi=105)
    plt.close(figure)


def write_frame_obj(path, half_extent, z_half):
    # 8-vertex box spanning the grid bounding box (+12 triangles); loader reads the verts' bbox.
    h, z = half_extent, z_half
    vertices = np.array([
        [-h, -h, -z], [-h, h, -z], [h, -h, -z], [h, h, -z],
        [-h, -h,  z], [-h, h,  z], [h, -h,  z], [h, h,  z]])
    faces = [(1, 2, 4), (1, 4, 3), (5, 7, 8), (5, 8, 6), (1, 5, 6), (1, 6, 2),
             (3, 4, 8), (3, 8, 7), (1, 3, 7), (1, 7, 5), (2, 6, 8), (2, 8, 4)]
    with open(path, "w") as handle:
        handle.write("# cavity frame bbox\n")
        for vertex in vertices:
            handle.write(f"v {vertex[0]:.6f} {vertex[1]:.6f} {vertex[2]:.6f}\n")
        for face in faces:
            handle.write(f"f {face[0]} {face[1]} {face[2]}\n")


