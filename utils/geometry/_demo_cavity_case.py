def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--half", type=int, default=1224,
                        help="fluid half-resolution: 0.5/dx (fluid is (2*half+1)^2 particles). "
                             "1224 -> ~6.0M, 500 -> ~1.0M, 1000 -> ~4.0M")
    parser.add_argument("--border", type=int, default=11, help="wall border layers")
    parser.add_argument("--lid", type=int, default=11, help="lid layers")
    parser.add_argument("--out", default="cases/lid_driven_cavity_2d_6m")
    parser.add_argument("--no-preview", action="store_true", help="skip split_preview.png")
    args = parser.parse_args()

    half_index = args.half
    dx = FLUID_HALF_EXTENT / half_index
    particle_radius = 0.5 * dx
    smoothing_length = 5.0 * dx
    border, lid = args.border, args.lid

    # Integer-indexed lattice so the border is EXACTLY `border` layers on every
    # side (a float region extent 0.5 + border*dx gets clipped off-by-one on some
    # sizes due to rounding). Site (kx, ky) sits at (kx*dx, ky*dx); the fluid box
    # is |k| <= half_index, surrounded by `border` index-rings.
    half_total = half_index + border
    index = np.arange(-half_total, half_total + 1)
    index_x, index_y = np.meshgrid(index, index, indexing="ij")
    index_x, index_y = index_x.ravel(), index_y.ravel()
    sites = np.column_stack([index_x * dx, index_y * dx])
    in_fluid = (np.abs(index_x) <= half_index) & (np.abs(index_y) <= half_index)
    is_lid = (index_y > half_index) & (np.abs(index_x) <= half_index)
    is_wall = (~in_fluid) & (~is_lid)
    region_half = half_total * dx

    domain = sites[in_fluid]
    lid_points = sites[is_lid]
    wall = sites[is_wall]
    n_fluid, n_wall, n_lid = domain.shape[0], wall.shape[0], lid_points.shape[0]
    total = n_fluid + n_wall + n_lid
    pool_size = int(math.ceil(total * 1.15 / 128) * 128)

    out_dir = _REPO_ROOT / args.out
    out_dir.mkdir(parents=True, exist_ok=True)
    print(f"dx={dx:.6e}  h={smoothing_length:.6e}  fluid box [-0.5,0.5]^2")
    print(f"domain(fluid)={n_fluid:,}  wall={n_wall:,}  wall_top(lid)={n_lid:,}  total={total:,}")
    print(f"pool_size={pool_size:,}  -> {out_dir}")

    write_obj(out_dir / "domain.obj", domain)
    write_obj(out_dir / "wall.obj", wall)
    write_obj(out_dir / "wall_top.obj", lid_points)
    frame_half = region_half + 0.6 * dx
    write_frame_obj(out_dir / "frame.obj", frame_half, particle_radius)
    (out_dir / "case.yaml").write_text(CASE_YAML.format(
        fluid_millions=n_fluid / 1e6, n_fluid=n_fluid, n_wall=n_wall, n_lid=n_lid,
        border=border, lid=lid, h=smoothing_length, dx=dx, particle_radius=particle_radius,
        pool_size=pool_size), encoding="utf-8")
    print("wrote frame.obj domain.obj wall.obj wall_top.obj case.yaml")
    if not args.no_preview:
        write_preview(out_dir / "split_preview.png", domain, wall, lid_points, dx, frame_half)
        print(f"wrote split_preview.png")
    return 0


if __name__ == "__main__":
    sys.exit(main())
